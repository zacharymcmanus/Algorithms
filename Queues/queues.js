// We're adding to the end and removing from the beginning - FIFO -- CONSTANT TIME
class Node {
    constructor(value) {
        this.value = value;
        this.next = null;
    }
}

class Queue {
    constructor() {
        this.first = null;
        this.last = null;
        this.size = 0;
    }

    // Accepts value, adds something to the end in
    enqueue(val) {
        var newNode = new Node(val);
        if(!this.first) {
            this.first = newNode;
            this.last = newNode;
        } else {
            this.last.next = newNode;
            this.last = newNode;
        }
        return ++this.size;
    }

    // Return and removes first thing added in to queue
    dequeue() {
        if(!this.first) return null;

        var temp = this.first;
        if(this.first === this.last) {
            this.last = null;
        }
        this.first = this.first.next;
        this.size--;
        return temp.value;
    }
}

var q = new Queue()
q.enqueue("First")
q.enqueue("Second")
q.enqueue("Third")
console.log(q)
q.dequeue()
